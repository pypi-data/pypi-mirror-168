def add_numbers(a,b):
    return a + b

def sub_numbers(a,b):
    return a - b

def mult_numbers(a,b):
    return a * b  

def div_numbers(a,b):
    return a / b

def mod_numbers(a,b):
    return a % b    

def squ_number(a, n):
    return a ** n 

def fdiv_numbers(a,b):
    return a // b                 