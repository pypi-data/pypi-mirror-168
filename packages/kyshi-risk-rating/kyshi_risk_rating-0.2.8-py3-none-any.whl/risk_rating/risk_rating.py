def __get_score(score, min, max):
    return ((score - min) * (score - max) <= 0)


def __compute_rating(score):
    for key in score_legend:
        truthy = __get_score(
            score, score_legend[key]['min'], score_legend[key]['max'])
        if truthy:
            return {
                "risk": key,
                "score": score
            }


def calculate_risk_score(payload: dict):

    nationality_rating = country_of_nationality_dict[payload.get(
        'country_of_nationality')]
    residence_rating = country_of_residence_dict[payload.get(
        'country_of_residence')]
    occupation_rating = industry_dict[payload.get('occupation', 'default')]
    wealth_rating = source_of_wealth_dict[payload.get(
        'source_of_wealth', 'employment')]
    risk_transaction_rating = high_risk_transaction_dict[payload.get(
        'high_risk_transaction', 'no')]
    pep_rating = pep_dict[payload.get('pep', 'no-pep-not-relevant-list')]

    score = nationality_rating['score'] + residence_rating['score'] + occupation_rating['score'] + \
        wealth_rating['score'] + \
        risk_transaction_rating['score'] + pep_rating['score']

    return __compute_rating(score / len(payload))
